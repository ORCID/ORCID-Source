package org.orcid.jaxb.model.record.summary_rc2;

import java.util.ArrayList;
import java.util.List;

import javax.xml.bind.annotation.XmlAccessType;
import javax.xml.bind.annotation.XmlAccessorType;
import javax.xml.bind.annotation.XmlElement;
import javax.xml.bind.annotation.XmlRootElement;
import javax.xml.bind.annotation.XmlType;

@XmlAccessorType(XmlAccessType.FIELD)
@XmlType(propOrder = { "identifier"})
@XmlRootElement(name = "identifiers", namespace = "http://www.orcid.org/ns/activities")
public class Identifiers {

    @XmlElement(name = "identifier", namespace = "http://www.orcid.org/ns/activities")
    private List<Identifier> identifier;

    public List<Identifier> getIdentifier() {
        if(identifier == null)
            identifier = new ArrayList<Identifier>();
        return identifier;
    }

    @Override
    public int hashCode() {
        final int prime = 31;
        int result = 1;
        result = prime * result + ((identifier == null) ? 0 : identifier.hashCode());
        return result;
    }

    @Override
    public boolean equals(Object obj) {
        if (this == obj)
            return true;
        if (obj == null)
            return false;
        if (getClass() != obj.getClass())
            return false;
        Identifiers other = (Identifiers) obj;
        if (identifier == null) {
            if (other.identifier != null)
                return false;
        } else if (!identifier.equals(other.identifier))
            return false;
        return true;
    }        
}
